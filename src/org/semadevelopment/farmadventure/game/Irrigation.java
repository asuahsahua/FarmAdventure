package org.semadevelopment.farmadventure.game;

public class Irrigation {
	private IrrigationDirection[] direction;
	private int x;
	private int y;
	
	public IrrigationDirection[] getDirection() {
		return direction;
	}
	public void setDirection(IrrigationDirection[] direction) {
		this.direction = direction;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
}
